public class Equipo {
    private int id;
    private String nombre;
    private DT DT;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public DT getDT() {
        return DT;
    }

    public void setDT(DT DT) {
        this.DT = DT;
    }
}
